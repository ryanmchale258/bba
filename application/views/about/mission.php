			<h1>How we do it</h1>
			<h2>Mission:</h2>
			<ul class="small-12 columns">
				<li>To be a leader in the field of dietetics and nutrition in long term care;</li>
				<li>To promote the role of the dietitian in the delivery of high quality nutrition and dietetic services</li>
				<li>To embrace a philosophy of care that recognizes the importance of nutrition to resident health and quality of life.</li>
			</ul>
			<h2>Values:</h2>
			<ul class="accordion" data-accordion>
			  <li class="accordion-navigation">
			    <a href="#panel1a"><h3>Resident-Centered Care...</h3></a>
			    <div id="panel1a" class="content active">
			      At BB&amp;A, we believe strongly in the value of a team approach to resident-centered care and work closely with staff in our Homes to provide quality care for residents.
			    </div>
			  </li>
			  <li class="accordion-navigation">
			    <a href="#panel2a"><h3>Continuity of Care...</h3></a>
			    <div id="panel2a" class="content">
			      To ensure continuity of care for the residents in our Homes, we provide coverage for vacation and other leaves, either on-site or on-call, when the primary Dietitian is away.  We can also provide additional hours, beyond the contracted monthly allocation, for special projects to enhance basic clinical nutrition care, as required.
			    </div>
			  </li>
			  <li class="accordion-navigation">
			    <a href="#panel3a"><h3>Commitment to Education...</h3></a>
			    <div id="panel3a" class="content">
			     	<span class="boldItalics">Home Staff Education</span><br>
					Our dietitians are committed to sharing their knowledge and expertise and are experienced in providing in-service education sessions for Dietary and Nursing staff.  All our Homes have access to our extensive roster of in-service sessions (compatible with on-line learning programs) as well as to members of our team with particular skills and expertise in specific clinical areas (e.g. diabetes management, dysphagia and enteral feeding).<br>
					<span class="boldItalics">Dietitians' Professional Development</span><br>
					With BB&amp;A, Homes link with the experience and knowledge of a group of dietitians who are devoted to long term care, participate in ongoing professional development activities, are members of the Dietitians of Canada Gerontology Network and meet regularly for education opportunities and to share ideas and resources.<br>
			    </div>
			  </li>
			  <li class="accordion-navigation">
			    <a href="#panel4a"><h3>Continuous Quality Improvement...</h3></a>
			    <div id="panel4a" class="content">
			     	On an annual basis, we provide a comprehensive written report of our activities and conduct an audit of our homes to receive feedback from Administrators, Nutrition/Food Service Managers and Nursing staff regarding the quality of services being provided.  We also conduct an internal audit in our Homes, as required, to evaluate the clinical nutrition care provided for residents by our dietitians and make improvements in any area identified.
			    </div>
			  </li>
			  <li class="accordion-navigation">
			    <a href="#panel5a"><h3>Open and Timely Communication...</h3></a>
			    <div id="panel5a" class="content">
			     	Our Dietitians communicate regularly, though verbal interchange and written reports, with staff in their Homes regarding resident nutrition care and other identified issues. The BB&amp;A Directors are accessible by phone or email so that contacting BB&amp;A is easy and convenient and any concerns or questions are dealt with promptly. As part of our commitment to communication and information sharing, we circulate relevant information, announcements and updates to all our LTC Homes on an ongoing basis, including new/revised BB&amp;A clinical and administrative policies and other valuable resources.
			    </div>
			  </li>
			  <li class="accordion-navigation">
			    <a href="#panel6a"><h3>Familiar, Fun and Fantastic Food...</h3></a>
			    <div id="panel6a" class="content">
			     	Along with our Dietitians, we work with the Home’s care team to encourage the idea of food being used to improve the Resident’s quality of life as well as nutritional status. The story and often the strong emotional connection that we all share with food is promoted. When possible, we also try to make food fun!
			    </div>
			  </li>
			</ul>
